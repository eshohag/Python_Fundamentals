print("Here is the one number {0} and another number {1} is Divided= {2}".format(aNumber,twoNumber,dividedOfTwoNumber))

multiplyOfTwoNumber=float(aNumber)*float(twoNumber);
print("Here is the one number {0} and another number {1} is Multiply= {2}".format(aNumber,twoNumber,multiplyOfTwoNumber))

powerOfTwoNumber=float(aNumber)**float(twoNumber);
